// Switch

var x = 2.5;

switch (x) {
    case 1:
        console.log('Number 1 Selected');
        break;
    case 2:
    case 2.5:
    case 2.7:
        console.log('Number 2 Selected');
        break;
    case 3:
        console.log('Number 3 Selected');
        break;
    case 4:
        console.log('Number 4 Selected');
        break;
    case 5:
        console.log('Number 5 Selected');
        break;

    default:
        console.log('Not in range');
        break;
}


if (x = 5) {
    console.log('number 5 Selected');
}

